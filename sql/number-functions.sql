SELECT
    ROUND(1.5) A,
    ROUND(2.5) B,
    ROUND(2.555, 2) C,
    TRUNC(1.5) D,
    TRUNC(2.5) E,
    TRUNC(2.555, 2) F,
    CEIL(1.5) G,
    CEIL(2.5) H,
    CEIL(-1.5) I,
    POWER(10,2) J
FROM dual;

